CREATE PROCEDURE [dbo].[INSERT_INVENTORY_RECORD] 
	@COMPANYID INT,
	@CATEGORYID INT,
	@ITEMID INT,
	@QUANTITY DECIMAL(9,2),
	@UNIT INT,
	@MEASUREMENT VARCHAR(10),
	@CREATEDDATE DATETIME,
	@CREATEDBY INT,
	@LASTUPDATED DATETIME,
	@UPDATEDBY INT,
	@MINIMUMSTOCK INT,
	@MAXIMUMSTOCK INT
AS
BEGIN
	INSERT INTO Inventory
	(
		CompanyId,
		CategoryId,
		ItemId,
		Quantity,
		Unit,
		Measurement,
		CreatedDate,
		CreatedBy,
		LastUpdated,
		UpdatedBy,
		MinimumStock,
		MaximumStock
	)
	VALUES
	(
		@COMPANYID,
		@CATEGORYID,
		@ITEMID,
		@QUANTITY,
		@UNIT,
		@MEASUREMENT,
		@CREATEDDATE,
		@CREATEDBY,
		@LASTUPDATED,
		@UPDATEDBY,
		@MINIMUMSTOCK,
		@MAXIMUMSTOCK
	);
END;